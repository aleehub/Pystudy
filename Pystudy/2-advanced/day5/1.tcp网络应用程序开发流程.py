# 1. tcp网络应用程序开发流程的介绍

# tcp网络应用程序开发分为：

# TCP客户端程序开发
# TCP服务端程序开发


# 2. TCP客户端程序开发流程


# 步骤说明:

# 1.创建客户端套接字对象
# 2.和服务端套接字建立连接
# 3.发送数据
# 4.接收数据
# 5.关闭客户端套接字


# 3. TCP服务端程序开发流程

# 步骤说明:

# 1.创建服务端端套接字对象
# 2.绑定端口号
# 3.设置监听
# 4.等待接受客户端的连接请求
# 5.接收数据
# 6.发送数据
# 7.关闭套接字

# 4. 小结

#   1.TCP 网络应用程序开发分为客户端程序开发和服务端程序开发。
#   2.主动发起建立连接请求的是客户端程序
#   3.等待接受连接请求的 是服务端程序